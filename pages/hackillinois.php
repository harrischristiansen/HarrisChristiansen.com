								After natural disasters occur, people often lose their forms of communication. Cellphone towers may go down and people don't know how to find shelter, food, etc. We set out to fix this issue with our hack, CloudScout.<br>
<br>
CloudScout delivers important information to victims of natural disasters and allows them to notify first responders + loved ones of their location and status. We do this using Quadcopters that can fly into disaster struck areas and provide temporary hotspots. These hotspots automatically redirect a connected users phone to our static webpage displaying up to date safety information for the specific event. In addition, our web page has a form for those that need immediate help or even want to send a text message or email to loved ones to let them know that they are ok.<br>
<br>
After the drone flies back to the operator, they are able to use our operator GUI to automatically send the requested messages and emails, and act upon the emergency messages sent from people in need of help. In addition, every time someone connects to the hotspot, we record the GPS location of where they connected so that emergency officials know about how many people are in different locations.
